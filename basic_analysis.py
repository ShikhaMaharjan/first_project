# A simple script to simulate calculating dataset averages
student_grades = [85, 92, 78, 90, 88, 95, 81]

# Calculate basic statistics
total_students = len(student_grades)
average_grade = sum(student_grades) / total_students

print(f"Data Analysis Results:")
print(f"Total Students Analyzed: {total_students}")
print(f"Average Class Grade: {average_grade:.2f}")
